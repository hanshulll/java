package inheritance;
public class Inheritance {
    public static void main(String[] args){
        SciCalc obj = new SciCalc();
        System.out.println(obj.add(2,44));
        System.out.println(obj.sub(100,34));
        System.out.println(obj.multiply(4,4));
        System.out.println(obj.divide(16,2));
        System.out.println(obj.pow(4,2));
    }
}
